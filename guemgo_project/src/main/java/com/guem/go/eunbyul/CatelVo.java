package com.guem.go.eunbyul;

public class CatelVo {
	public int 	l_num;
	public String l_name;
	
	public CatelVo() {}

	public CatelVo(int l_num, String l_name) {
		super();
		this.l_num = l_num;
		this.l_name = l_name;
	}

	public int getL_num() {
		return l_num;
	}

	public void setL_num(int l_num) {
		this.l_num = l_num;
	}

	public String getL_name() {
		return l_name;
	}

	public void setL_name(String l_name) {
		this.l_name = l_name;
	}
	
	
}
